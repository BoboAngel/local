public class IntDatali{
	public static void main(String[] args){
		Long a = 3L;//长整型Long 赋值后要加L或l
		float num = 1.3F;//浮点型float赋值后要加F或f，计算机默认double双精度8字节，
		//float转换double依然不删除后缀F；
		double num2 = 1.3;
		double num3 = .244;//等价于0.244
	System.out.println(a);
	System.out.println(num);
	System.out.println(num2);

	}
}